/**
 * 分析文档内容，提取常见术语
 */
async function analyzeDocumentTerms() {
    console.log('正在分析文档内容...\n');

    try {
        const data = fs.readFileSync('./document_chunks.json', 'utf-8');
        const chunks = JSON.parse(data);

        const termFrequency = {};

        // 统计词频
        chunks.forEach(chunk => {
            const text = chunk.text || '';
            const words = text.match(/[\u4e00-\u9fa5]{2,}/g) || [];

            words.forEach(word => {
                termFrequency[word] = (termFrequency[word] || 0) + 1;
            });
        });

        // 排序并取前100个
        const sortedTerms = Object.entries(termFrequency)
            .sort((a, b) => b[1] - a[1])
            .slice(0, 100);

        console.log('文档中最常见的100个词汇：');
        console.log('─'.repeat(80));
        sortedTerms.forEach(([term, count], index) => {
            console.log(`${(index + 1).toString().padStart(3, ' ')}. ${term.padEnd(12, ' ')} 出现次数: ${count}`);
        });

        return sortedTerms;

    } catch (error) {
        console.error('分析失败：', error.message);
        return [];
    }
}

