function normalizeQuestion(question) {
    let normalized = question;

    // 替换所有同义词
    for (const [colloquial, formal] of Object.entries(SYNONYM_MAP)) {
        const regex = new RegExp(colloquial, 'g');
        normalized = normalized.replace(regex, formal);
    }

    return normalized;
}

